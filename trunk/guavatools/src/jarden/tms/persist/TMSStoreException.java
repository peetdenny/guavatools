package jarden.tms.persist;

public class TMSStoreException extends RuntimeException {

    public TMSStoreException() {
        super();
    }

    public TMSStoreException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public TMSStoreException(String arg0) {
        super(arg0);
    }

    public TMSStoreException(Throwable arg0) {
        super(arg0);
    }

}
